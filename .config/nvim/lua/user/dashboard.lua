vim.api.nvim_create_autocmd("Filetype", {
	pattern = "dashboard",
	group = vim.api.nvim_create_augroup("Dashboard_au", { clear = true }),
	callback = function()
		vim.cmd([[
            setlocal buftype=nofile
            setlocal nonumber norelativenumber nocursorline noruler
        ]])
	end,
})

